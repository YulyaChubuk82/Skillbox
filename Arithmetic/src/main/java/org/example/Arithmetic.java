package org.example;

public class Arithmetic {
    public int x = 5;
    public int y = 10;
    public int sum() {

        return x + y;
    }
    public int product() {
        return x * y;
    }
    public int max() {

        return Math.max (x, y);
    }
    public int min() {

        return Math.min (x, y);
    }
}
